a = 10
b = 5

addition = a + b
subtraction = a - b
multiplication = a * b
# returns floating numbers
division = a / b

# returns integer numbers
double_div = a // b

# returns reminder of dividion
modulus = a % b

# exponenet(power)
exponent = 4 ** 3
print(f"add = {addition}")
print(f"minus = {subtraction}")
print(f"multi = {multiplication}")
print(f"div = {division}")
print(f"double_div = {double_div}")
print(f"modulus = {modulus}")
print(f"exponent = {exponent}")