
# first do paranthesis -> div -> multi
x = 10 + 3 * 2
# 6 + 10
print(f"x = {x}")

y = (10 + 3) * 2
# 13 * 2 = 26
print(f"y = {y}")

z = (10 + 3) / 2
# 13/2 = 6.5
print(f"z = {z}")

a = 10 * 4 / 2
# 10 * 2 = 20.0
print(f"a = {a}")

b = 10 ** 2 / 2
# 100 / 2 = 50
print(f"b = {b}")

c = 10 ** (2 + 2)
# 10 ** 4 = 10000
print(f"c = {c}")